int		ft_str_is_alpha(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] < 65 || (str[i] >= 91 && 96 >= str[i]) || str[i] > 122)
			return (0);
		i++;
	}
	return (1);
}
