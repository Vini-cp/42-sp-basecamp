int		*ft_strcpy(int *dest, char *src)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (src[i] != '\0')
	{
		if (src[i] > 32)
		{
			dest[j] = src[i] - '0';
			j++;
		}
		i++;
	}
	return (dest);
}
