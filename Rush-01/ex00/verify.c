int		verify(int *ptr)
{
	int i;

	i = 0;
	while (i < 4)
	{
		if (ptr[i] + ptr[i + 4] < 3 || ptr[i] + ptr[i + 4] > 5 || ptr[i + 8]
			+ ptr[i + 12] < 3 || ptr[i + 8] + ptr[i + 12] > 5)
			return (0);
		i++;
	}
	i = 0;
	while (i < 16)
	{
		if (ptr[i] < 1 || ptr[i] > 4)
			return (0);
		i++;
	}
	return (1);
}
